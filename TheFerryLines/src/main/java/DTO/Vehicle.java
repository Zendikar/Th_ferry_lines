package DTO;

/**
 *
 * @author Tobias
 */
public class Vehicle {
    String type;
    String size;

    public Vehicle(String type, String size) {
        this.type = type;
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
}
